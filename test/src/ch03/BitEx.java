package ch03;

public class BitEx {

	public static void main(String[] args) {
		
		//비트연산자 ( | or, & and, ^ XOR)
		System.out.println(3 | 5);// or연산(긍정적인 아이) 0011 | 0101 =>0111(십진수7)
		System.out.println(3 & 5);// and연산(부정적인 아이) 0011 & 0101 =>0001(십진수1)
		System.out.println(3 ^ 5);// Xor연산(배타적인 아이) 0011 ^ 0101 =>0110(십진수6)

	}

}
